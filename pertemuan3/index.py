# ============ Belajar Concatination, Escaping & Method Pada String ============
# A. Concatination
#    Concatiation merupakan cara kita untuk 
#    menggabungkan dua string atau lebih.
#    Dalam Python kita menggunakan tanda '+'
#    untuk melakukan concatination.

# Concatination
nama = 'Mathius'
marga = 'Kormasela'
print(nama + ' ' + marga)

# Escaping
print("Mathius \"Kormasela\"")

# Membuat baris baru
print("Mathius \n Kormasela")

# membuat tab
print("Mathius \t KOrmaselaa")

# Mengubah huruf depan suatu string menjadi kapital
bhs = 'python'
print(bhs.capitalize())

# Mengubah sting
nama2 = 'Mathius'
print(nama2.replace('th', 't'))